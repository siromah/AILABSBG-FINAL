export function getMissionById(id: string): Mission | undefined {
  return MISSION_MODULES.flatMap((m) => m.missions).find((m) => m.id === id);
}

export function getModuleForMission(missionId: string): LearningModule | undefined {
  return MISSION_MODULES.find((m) => m.missions.some((mission) => mission.id === missionId));
}
